import router from '@/router/index.js'
// 获取当前路由
const getCurrentRoute = () => {
    const route = router.currentRoute.value
    return route.fullPath // 获取当前路由路径
}

const getRouteKey = () => {
    const currentRoute = getCurrentRoute()
    // 基础转换：去除开头斜杠，替换中间斜杠为下划线
    let routeKey = currentRoute.replace(/^\//, '').replace(/\//g, '_')

    // 关键修改：将非贪婪匹配+?改为贪婪匹配+，确保匹配到最后一个符合条件的后缀
    const suffixMatch = routeKey.match(/^(.+_(list1|list2|list3|list|upd|add|info|copyAdd|myImport)).*$/)
    if (suffixMatch) {
        routeKey = suffixMatch[1]
    }

    return routeKey
}
// 获取列文字
export const getColumnText = (code) => {
    if (!code) return '';

    const routeKey = getRouteKey();

    // 1. 尝试查找页面专属列名: columns.base_wh_location.code
    const pageKey = `columns.${routeKey}.${code}`;
    if (te(pageKey)) {
        return t(pageKey);
    }

    // 2. 尝试查找通用列名: columns.general.code (假设你有一个通用的定义)
    // 如果原来的 general 没有迁移过来，这一步可以先省略，或者手动在 generated.json 里加一个 general 节点
    const commonKey = `columns.general.${code}`;
    if (te(commonKey)) {
        return t(commonKey);
    }

    // 3. 兜底：直接返回 code，方便你看到哪里没翻译
    return code;
}

